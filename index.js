// Function without parameters, without return.
function add() {
    console.log('Hi')
}
add();  //Hi


// Function with params, without return.
function name(userName) {
    console.log(userName);
}
name('Deepak'); //Chetan


// Function with params, with return.
function multiply(a, b) {
    return a * b;
}
console.log(multiply(3, 5)); //15
